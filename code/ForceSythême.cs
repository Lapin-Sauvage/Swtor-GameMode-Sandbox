        public void SysthemeForce()
        {
            // Event Moove qui va add des point d'age
            string gradeforce;
            var force_point = 0.000;

            if (force_point == 100.000)
            {
                Console.WriteLine("Vous avez atteint les 100 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Adepte de la force ";
            }

            if (force_point == 1000.000)
            {
                Console.WriteLine("Vous avez atteint les 1000 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Savant de la force";
            }

            if (force_point == 5000.000)
            {
                Console.WriteLine("Vous avez atteint les 1000 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Maitriseur de la force";
            }

            if (force_point == 10000.000)
            {
                Console.WriteLine("Vous avez atteint les 1000 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Grand Adepte de la force";
            }

            if (force_point == 20000.000)
            {
                Console.WriteLine("Vous avez atteint les 1000 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Grand Savant de la force";
            }

            if (force_point == 30000.000)
            {
                Console.WriteLine("Vous avez atteint les 1000 points de force ! Vous êtes de plus en plus puissant ...");
                gradeforce = "Grand Maitriseur de la force";
            }

        }
    }
